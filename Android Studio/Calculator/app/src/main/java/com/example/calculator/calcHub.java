package com.example.calculator;

public class calcHub {

    public static double addCalc(double a,double b){
        return a+b;
    }

    public static double subCalc(double a,double b){
        return a-b;
    }

    public static double mulCalc(double a,double b){
        return a*b;
    }

    public static double divCalc(double a,double b){
        return a/b;
    }

    public static double powCalc(double a, double b){
        return Math.pow(a, b);
    }

}
