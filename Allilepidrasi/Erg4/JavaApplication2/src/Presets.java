public class Presets 
{
    int bass ; 
    int midrange ;
    int tremble ; 
    int balance ; 
    int volume ;
    public Presets(int a, int b, int c, int d, int e) 
    {
        this.bass = a ; 
        this.midrange = b ; 
        this.tremble = c ; 
        this.balance = d ;
        this.volume = e ; 
    }   
}
